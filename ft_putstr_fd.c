#include "ft_printf.h"

int		ft_putstr_fd(char *str, int fd)
{
	int i;

	i = 0;
	if (str)
		while (str[i] != '\0')
		{
			write(fd, &str[i], 1);
			i++;
		}
	return (i);
}
