#include "ft_printf.h"

int		ft_atoi(const char *str)
{
	long	r;
	int		i;
	long	sg;

	sg = 1;
	r = 0;
	i = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		sg = sg * (-1);
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (r <= ((9223372036854775807 - (str[i] - 48)) / 10))
			r = r * 10 + (str[i] - 48);
		else
		{
			r = (sg == -1) ? 0 : -1;
			break ;
		}
		i++;
	}
	return ((int)(r * sg));
}
